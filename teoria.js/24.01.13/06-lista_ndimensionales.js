const lista = [
  [1, 2, 3, 4],
  [5, 6, 7, 8],
]

/*la primea lista contiene su primer indice 0
la segunda lista contiene su primer indice 1 */

console.log (lista[0]); /*nos saca la primera lista */
console.log (lista[1]); /* nos saca la sengunda lista, porque son listas que contienen listas */

/* como podriamos acceder al primer elemento del primer elemento? */
